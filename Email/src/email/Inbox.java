    public static void check(DefaultTableModel dtm,String protocol,String host,String port) 
   {
      try {

      //create properties field
        Properties properties = new Properties();
        properties.put(String.format("mail.%s.host", protocol), host);
        properties.put(String.format("mail.%s.port", protocol), port);
 
        // SSL setting
        properties.setProperty(
                String.format("mail.%s.socketFactory.class", protocol),
                "javax.net.ssl.SSLSocketFactory");
        properties.setProperty(
                String.format("mail.%s.socketFactory.fallback", protocol),
                "false");
        properties.setProperty(
                String.format("mail.%s.socketFactory.port", protocol),
                String.valueOf(port));
//        properties.put(String.format("mail.%s.starttls.enable",protocol), "true");
      Session emailSession = Session.getDefaultInstance(properties);
  
      //create the POP3 store object and connect with the pop server
      Store store = emailSession.getStore(protocol);

      store.connect(host, mailid, pass);

      //create the folder object and open it
      Folder emailFolder = store.getFolder("INBOX");
      emailFolder.open(Folder.READ_ONLY);

      // retrieve the messages from the folder in an array and print it
      Message[] messages = emailFolder.getMessages();

      for (int i = 0, n = messages.length; i < n; i++) {
         Message message = messages[i];
         Object data1=(i + 1);
         Object data3=message.getSubject();
         Object data2=message.getFrom()[0];
         Object row[]={data1,data2,data3};
         dtm.addRow(row);
      }

      //close the store and folder objects
      emailFolder.close(false);
//      store.close();
      } catch (Exception e) {
         JOptionPane.showMessageDialog(null, "Exception Occured");
      }
   }

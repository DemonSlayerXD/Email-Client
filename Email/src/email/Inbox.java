        DefaultTableModel dtm=(DefaultTableModel) inboxtable.getModel();
        dtm.setRowCount(0);
        String ser=(String)serverBox.getSelectedItem();
        if("POP3".equals(ser))
        {
            String protocol = "pop3";
            String host = "pop.gmail.com";
            String port = "995";
            check(dtm, protocol, host, port);
        }
        else
        {
            String protocol = "imap";
            String host = "imap.gmail.com";
            String port = "993";
            check(dtm, protocol, host, port);
        }
